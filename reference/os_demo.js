const os = require('os');

//Platform 
console.log(os.platform());

//CPU arch
console.log(os.arch());

//CPU core
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//Total memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());

//Up time
console.log(os.uptime());